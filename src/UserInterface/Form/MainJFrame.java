    public static String obtenerFechaFormateada() {
        LocalDate hoy = LocalDate.now(); // obtiene la fecha actual

        String nombreDia = hoy.getDayOfWeek().getDisplayName(TextStyle.FULL, new Locale("es", "ES"));
        int numeroDia = hoy.getDayOfMonth();
        String nombreMes = hoy.getMonth().getDisplayName(TextStyle.FULL, new Locale("es", "ES"));
        int a単o = hoy.getYear();

        return nombreDia.toUpperCase() + ", " + numeroDia + " de " + nombreMes + " de " + a単o;
    }
    
